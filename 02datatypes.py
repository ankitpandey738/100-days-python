'''Python supports 3 categories of data types:
--> Basic Types- (integer,float,complex,boolean and string)
--> Container Types -(List,Tuples,Sets and Dictionary)
--> User-defined Types- Class
'''


# Type-1 Basic Types
# integer
print(4)
print(1e308)
print(1e309)

# float
print(4.5)
print(1.7e308)
print(1.7e309)

# boolean
print(True)
print(False)

# complex
print(4+5j)

# string
print('Kolkata')
print("Kolkata")
print("""Kolkata""")


#Type-2 Container Types

# list (In other languages list is also known as "Array")
print([1,2,3,4,5,6])

# tuple  (In list we use square bracket to make the list and in tuple we use the small bracket to make the tuples)
print((1,2,3,4,5))

# sets
print({1,2,3,4,5,6})

# Dictionary  (In dictionary searching is fast)
print({"Name":"Ankit", "Age":22, "gender":"Male"})